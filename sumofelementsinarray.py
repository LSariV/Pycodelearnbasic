  # example : if we have array we can find sum by function 

arr = [1,2,3,4,5]

print("sum of given array is ", sum(arr))  # it gives 15

# adding some value to the sum of array

print("sum of given array + adding 10 to it is " , sum(arr,10)) # 15+10

# subtracting some value from the sum of array

print("subtracting from given sum of array is " , sum(arr,-10)) # 15-10

